//程序：CH02_08.java，数据类型的转换
public class CH02_08 {
    public static void main(String[ ] args) {
        int i=10;			
        byte b=(byte) i;	
        byte b1=65;			
        char c=(char)b1;	
        System.out.println("i="+i);
        System.out.println("b="+b);
        System.out.println("b1="+b1);
        System.out.println("c="+c);
    }
}