public class CH02_10 {
    public static void main(String args[]) {
        System.out.print("15大于5 为"+(15>5)+"\n");
        System.out.print("15小于5 为"+(15<5)+"\n");
        System.out.print("15大于等于15 为"+(15>=15)+"\n");
        System.out.print("15小于等于5 为"+(15<=5)+"\n");
        System.out.print("15不等于5 为"+(15!=5)+"\n");
        System.out.print("15等于5 为"+(15==5)+"\n");
    }
}